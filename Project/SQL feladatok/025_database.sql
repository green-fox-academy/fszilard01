/*
o Adatbázis neve: INTERFACE 
o Tulajdonos: interface_svc 
o Recovery model: FULL 
o Induló adatbázis file: 
o méret: 32MByte 
o helye: S:\SQLDATA\DB 
o Induló log file: 
o méret: 16MByte 
o helye: S:\SQLDATA\LOG */


